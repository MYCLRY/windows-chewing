#include "Window.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

Window::Window()
{

}

Window::~Window()
{
	if(hwnd)
		DestroyWindow(hwnd);
}

void Window::destroy(void)
{
	if( hwnd )
		DestroyWindow(hwnd);
	hwnd = NULL;
}
